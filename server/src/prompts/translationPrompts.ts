/**
 * ==========================================================
 * 翻译提示词工厂 (Agent2 专用)
 * ==========================================================
 *
 * 设计思路：
 * 1. 根据源角色和目标角色的组合，生成针对性的翻译提示词
 * 2. 通过角色思维画像让 AI 深入理解不同岗位的思考模式差异
 * 3. 通过源角色盲区提示，引导 AI 主动补全目标角色关心的缺失信息
 * 4. 翻译输出格式按目标角色的关注维度组织，确保信息"说到点上"
 * 5. 保留原始信息的核心意图，避免信息丢失
 *
 * Prompt 工程要点：
 * - ROLE_PROFILES: 描述每种角色的思考路径、决策依据和沟通偏好
 * - ROLE_BLINDSPOTS: 标注源角色表述中通常缺失的信息维度
 * - TARGET_FORMAT_GUIDE: 按目标角色的关注维度定义输出框架
 * - 注入 Agent1 识别的 keyPoints，让翻译有据可依
 * - 中等温度 (0.6) 允许翻译有一定灵活度
 */
import { Role, ROLE_LABELS, ROLE_FOCUS } from '../types/index.js';

/**
 * 角色思维画像 — 帮助 AI 深入理解每种角色的思考方式和沟通习惯
 *
 * 每个画像包含：思考路径、决策依据、沟通偏好、核心关切
 * 这让 AI 不仅知道"角色是什么"，更理解"角色怎么想问题"
 */
const ROLE_PROFILES: Record<Role, string> = {
  product_manager: `**产品经理的思维模式**：
- 思考路径：用户是谁 → 痛点是什么 → 如何解决 → 带来什么价值
- 决策依据：用户调研数据、竞品分析、北极星指标（留存/转化/NPS）
- 沟通偏好：用"用户故事"和场景化描述，重"为什么做"和"做什么"，轻"怎么做"
- 核心关切：这解决了什么用户问题？对哪个业务指标有帮助？优先级怎么排？
- 典型口头禅："从用户角度来看..."、"核心价值是..."、"MVP先做..."、"竞品是怎么做的"`,

  developer: `**开发工程师的思维模式**：
- 思考路径：输入是什么 → 处理逻辑 → 输出是什么 → 边界条件与异常处理
- 决策依据：技术可行性、系统架构约束、性能基准、代码复杂度与维护成本
- 沟通偏好：精确的技术描述，需要明确的输入/输出/边界条件，重"怎么做"和"要多久"
- 核心关切：技术方案是什么？性能要求多高？工作量多大？有什么兼容性问题？
- 典型口头禅："并发瓶颈在..."、"需要考虑兼容性..."、"技术上可行但..."、"预估X人天"`,

  operations: `**运营的思维模式**：
- 思考路径：目标用户是谁 → 怎么触达 → 转化路径 → 数据度量 → 迭代优化
- 决策依据：渠道ROI、转化漏斗数据、用户分层画像、A/B测试结果
- 沟通偏好：数据驱动，用指标量化一切，重"影响多少用户"和"指标变化多少"
- 核心关切：对DAU/留存有什么影响？目标用户群体是谁？能配合做什么活动？
- 典型口头禅："预计带来X%的提升..."、"目标用户群是..."、"可以配合做个活动..."`,

  management: `**管理层的思维模式**：
- 思考路径：投入多少 → 预期回报 → 时间节点 → 风险可控性 → 战略匹配度
- 决策依据：ROI、市场竞争态势、组织产能、战略优先级排序
- 沟通偏好：结论先行，要"电梯演讲式"简洁汇报，不关心技术细节，重"值不值得做"
- 核心关切：ROI是多少？需要多少人和多长时间？主要风险是什么？和战略怎么对齐？
- 典型口头禅："ROI是多少"、"需要投入多少资源"、"和Q2目标的关系"、"主要风险是什么"`
};

/**
 * 源角色表述盲区 — 标注每种角色在表述时通常缺失的信息维度
 *
 * 翻译时需要根据这些盲区，主动补全目标角色关心但原文未提及的内容
 */
const ROLE_BLINDSPOTS: Record<Role, string> = {
  product_manager: `产品经理的表述通常缺少：具体技术实现方案和架构影响、精确的性能指标要求（QPS/延迟）、开发工作量和排期细节、财务成本估算和ROI量化。其表述偏重定性的用户价值，缺少技术和财务维度的量化信息。`,

  developer: `开发工程师的表述通常缺少：为什么做这件事的业务背景和用户价值、对业务指标（留存/转化/营收）的影响评估、跨部门的优先级排序依据、对非技术同事的通俗解释。其表述偏重技术实现细节和复杂度分析，缺少"为什么"和"价值多大"的商业视角。`,

  operations: `运营的表述通常缺少：技术实现可行性和架构影响评估、开发资源成本和工时预估、产品长期规划的全局视角、系统性能约束和技术依赖关系。其表述偏重短期数据指标和增长策略，缺少对技术约束和产品全局的考量。`,

  management: `管理层的表述通常缺少：具体执行路径和落地方案、技术方案选型和可行性分析、详细的用户场景和痛点描述、日常运营节奏和过程指标。其表述偏重战略方向和结果目标，缺少从目标到执行的具体拆解。`
};

/**
 * 针对不同目标角色的翻译输出格式指引
 *
 * 每个维度都带有引导性问题，帮助 AI 知道具体该回答什么，
 * 而不只是给出维度名称。
 */
const TARGET_FORMAT_GUIDE: Record<Role, string> = {
  product_manager: `请按产品经理最关注的维度组织翻译结果：
- **用户价值**：解决了什么用户痛点？哪些用户场景受益？用户体验如何改善？
- **商业影响**：对核心业务指标（留存/转化/营收/NPS）的预期影响是什么？
- **功能理解**：从产品功能角度这意味着什么变化？需要怎样的产品设计？
- **优先级判断**：相对其他需求的重要程度和紧迫度如何？建议放在哪个迭代？`,

  developer: `请按开发工程师最关注的维度组织翻译结果：
- **技术需求**：具体要实现什么？输入/输出/边界条件是什么？涉及哪些接口和模块？
- **技术方案**：推荐的技术选型和架构方案是什么？有没有可复用的现有模块？
- **性能与约束**：QPS/延迟/并发/存储有什么具体要求？有哪些兼容性约束和系统依赖？
- **工作量评估**：预估多少人天？建议如何拆分子任务和排定里程碑？
- **技术风险**：有哪些潜在的技术风险和边界情况？对现有系统稳定性有什么影响？`,

  operations: `请按运营最关注的维度组织翻译结果：
- **用户影响**：影响哪些用户群体（新/老/高价值）？用户行为路径会发生什么变化？
- **数据指标**：预估对关键指标（DAU/留存/转化/ARPU/LTV）的影响幅度是多少？
- **运营配合**：运营侧需要做什么准备？可以策划什么推广活动或用户引导方案？
- **增长机会**：带来哪些新的增长切入点？能否利用现有渠道和用户基础放大效果？`,

  management: `请按管理层最关注的维度组织翻译结果：
- **商业价值**：预期 ROI 是多少？对营收/利润/市场份额的量化影响？
- **资源投入**：需要的人力（几人×多少周）、预算和其他资源投入？
- **项目节奏**：关键里程碑和时间节点？多久能看到第一批可验证的成果？
- **风险评估**：Top 3 风险是什么？发生概率、影响程度和应对预案？
- **战略对齐**：与本季度/年度战略目标的关联度？和竞品相比的差异化定位？`
};

/**
 * 构建翻译 Prompt
 *
 * @param content - 用户输入的原始内容
 * @param sourceRole - 发言者角色（由 Agent1 识别或用户手动指定）
 * @param targetRole - 目标受众角色
 * @param keyPoints - Agent1 提取的关键要点列表
 * @returns 完整的翻译 Prompt，包含角色画像、盲区提示、翻译原则和输出格式
 */
export function buildTranslationPrompt(
  content: string,
  sourceRole: Role,
  targetRole: Role,
  keyPoints: string[]
): string {
  const sourceLabel = ROLE_LABELS[sourceRole];
  const targetLabel = ROLE_LABELS[targetRole];
  const sourceFocus = ROLE_FOCUS[sourceRole].join('、');
  const targetFocus = ROLE_FOCUS[targetRole].join('、');
  const formatGuide = TARGET_FORMAT_GUIDE[targetRole];
  const sourceProfile = ROLE_PROFILES[sourceRole];
  const targetProfile = ROLE_PROFILES[targetRole];
  const sourceBlindspot = ROLE_BLINDSPOTS[sourceRole];
  const keyPointsStr =
    keyPoints.length > 0 ?
      `\n## 已识别的关键点\n${keyPoints.map((p, i) => `${i + 1}. ${p}`).join('\n')}`
    : '';

  return `你是一位在产品、技术、运营和管理岗位都有多年实战经验的跨职能沟通专家。你深刻理解不同岗位的思维模式、关注重点和沟通习惯之间的差异。

## 你的任务

将**${sourceLabel}**的表述翻译成**${targetLabel}**能够轻松理解、直接使用的语言。不是简单的文字转换，而是站在${targetLabel}的视角重新组织信息架构。

## 角色思维画像

${sourceProfile}

${targetProfile}

## 沟通背景

- **发言者**：${sourceLabel}（关注：${sourceFocus}）
- **目标受众**：${targetLabel}（关注：${targetFocus}）
- **信息盲区**：${sourceBlindspot}
${keyPointsStr}

## 翻译原则

1. **视角转换**：不只是换术语，而是换整个思考框架——用${targetLabel}的决策逻辑重新组织信息
2. **术语翻译**：将${sourceLabel}的专业术语转化为${targetLabel}日常使用的概念和度量方式
3. **盲区补全**：针对${sourceLabel}表述中的信息盲区，主动补充${targetLabel}关心但原文未提及的内容
4. **量化表达**：尽量将定性描述转化为${targetLabel}熟悉的量化指标或可衡量标准
5. **结论先行**：先给出${targetLabel}最关心的结论，再展开支撑细节

## 输出格式

${formatGuide}

每个维度下写 1-3 句简洁但信息量充分的内容。使用 Markdown 格式。如果原始内容未提供某维度的信息，基于合理推断进行补充并标注"（推断）"。

## 原始内容（${sourceLabel}的表述）

${content}`;
}
