/**
 * ==========================================================
 * 意图识别提示词 (Agent1 专用)
 * ==========================================================
 *
 * 设计思路：
 * 1. 要求模型输出固定 JSON schema，便于程序解析
 * 2. 提供 4 种角色的典型术语和表述风格作为判断依据
 * 3. 要求返回 top-2 候选角色及置信度，支持三级降级：
 *    - confidence >= 0.8 → 自动确认
 *    - 0.4 <= confidence < 0.8 → 推荐 top-2 候选让用户确认
 *    - confidence < 0.4 → 手动选择
 * 4. 同时识别场景类型（需求讨论/技术方案/运营策略/管理决策）
 * 5. 提取 2-5 个关键点 (keyPoints)，传递给 Agent2 辅助翻译
 *
 * Prompt 工程要点：
 * - 列出各角色的典型术语列表，帮助模型建立分类基准
 * - 用 JSON schema 约束输出格式，减少解析失败率
 * - 低温度 (0.2) 调用确保输出稳定性
 */
import { Role, SceneType, ROLE_LABELS } from '../types/index.js';
/**
 * 构建意图识别 Prompt
 *
 * @param content - 用户输入的原始职场沟通内容
 * @returns 完整的 Prompt 字符串，包含角色术语列表、场景分类、JSON 输出要求
 *
 * 输出 JSON 结构:
 * {
 *   sourceRole: Role,          // 最可能的角色
 *   confidence: number,        // 0-1 之间的置信度
 *   candidates: [{role, confidence}], // top-2 候选
 *   sceneType: SceneType,      // 场景类型
 *   keyPoints: string[]        // 2-5 个核心要点
 * }
 */
export function buildIntentPrompt(content: string): string {
  return `你是一个专业的职能角色识别专家。你的任务是分析一段职场沟通内容，判断发言者最可能的角色和沟通场景。

## 角色类型（4种）

1. **product_manager（产品经理）**
   - 典型术语：用户需求、用户体验、留存率、转化漏斗、功能优先级、MVP、用户故事、需求文档、交互设计、用户价值、商业目标、竞品分析、PRD、P0/P1、北极星指标、验收标准
   - 思维特征：从"用户"出发思考问题，关注"为什么做"和"做什么"，较少涉及具体实现细节
   - 表述风格：善用场景化描述（"当用户...时"），喜欢讨论优先级和价值排序，常引用数据支撑观点

2. **developer（开发工程师）**
   - 典型术语：QPS、TPS、数据库、API、微服务、重构、代码审查、技术债、缓存、索引、并发、延迟、吞吐量、架构、部署、SDK、灰度、回滚、SLA、CI/CD
   - 思维特征：从"系统"出发思考问题，关注"怎么做"和"做多久"，注重边界条件和异常处理
   - 表述风格：用精确的技术语言描述方案，会分析复杂度和可行性，常给出工时估算

3. **operations（运营）**
   - 典型术语：DAU、MAU、获客成本、ARPU、活动策划、投放ROI、用户增长、裂变、私域、转化率、渠道、复购率、用户分层、LTV、A/B测试、推送、同期群
   - 思维特征：从"数据"出发思考问题，关注"影响多少用户"和"指标变化多少"，偏好量化表达
   - 表述风格：数据驱动，习惯用指标和漏斗描述问题，常提到目标用户群和渠道策略

4. **management（管理层）**
   - 典型术语：ROI、预算、里程碑、OKR、KPI、资源分配、风险评估、战略规划、人力成本、组织架构、竞争格局、市场份额、季度目标、汇报
   - 思维特征：从"全局"出发思考问题，关注"值不值得做"和"投入产出比"，需要结论先行
   - 表述风格：偏好简洁概括性的表述，关注结果而非过程，常提到时间节点和资源约束

## 场景类型（4种）

- **requirement_discussion**：需求讨论 — 讨论功能需求、用户故事、产品规划
- **technical_solution**：技术方案 — 讨论技术选型、架构设计、性能优化
- **operations_strategy**：运营策略 — 讨论增长方案、活动策划、数据分析
- **management_decision**：管理决策 — 讨论资源分配、项目排期、战略方向

## 输出要求

请严格输出以下 JSON 格式，不要输出其他内容：

\`\`\`json
{
  "sourceRole": "最可能的角色ID",
  "confidence": 0.85,
  "candidates": [
    { "role": "最可能的角色ID", "confidence": 0.85 },
    { "role": "第二可能的角色ID", "confidence": 0.10 }
  ],
  "sceneType": "场景类型ID",
  "keyPoints": ["关键点1", "关键点2", "关键点3"]
}
\`\`\`

其中：
- confidence 是 0-1 之间的浮点数
- candidates 包含 top-2 候选角色，按置信度降序
- keyPoints 提取输入中的 2-5 个核心要点

## 待分析内容

${content}`;
}
